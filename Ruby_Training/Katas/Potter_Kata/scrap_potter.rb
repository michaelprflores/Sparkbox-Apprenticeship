# Mission is to create sets of discounts for books

if I buy 1 book from [1, 2, 3, 4, 5] the cost is $8
if I buy 2 unique books it is 5% off the 2 books
if I buy 3 unique books it is 10% off the 3 books
if I buy 4 unique books it is 20% off the 4 books
if I buy 5 unique books it is 25% off the 5 books


